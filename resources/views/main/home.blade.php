                              $("#loadMoreBtnContainer").html("No more products to load");
                          }
                      } else {
                          $("#loadMoreBtnContainer").html("Error loading products");
                      }
                  },
                  complete: function() {
                      loading = false;
                      $("#loadMoreBtn").html("Load More");
                  }
